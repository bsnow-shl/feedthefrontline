// [START calendar_quickstart]
require __DIR__ . '/vendor/autoload.php';
include 'getclient.php';

if (php_sapi_name() != 'cli') {
    throw new Exception('This application must be run on the command line.');
}


/*

Donor page must allow anyone to add an item to a calendar.  Structured fields
will turn into mostly free text, human readable
Need: a date, and one of 4 times (breakfast, lunch, dinner, midnight)

*/




// Get the API client and construct the service object.
$client = getClient();
$service = new Google_Service_Calendar($client);

// Print the next 10 events on the user's calendar.
$calendarId = 'primary';
$optParams = array(
  'maxResults' => 10,
  'orderBy' => 'startTime',
  'singleEvents' => true,
  'timeMin' => date('c'),
);
$results = $service->events->listEvents($calendarId, $optParams);
$events = $results->getItems();

if (empty($events)) {
    print "No upcoming events found.\n";
} else {
    print "Upcoming events:\n";
    foreach ($events as $event) {
        $start = $event->start->dateTime;
        if (empty($start)) {
            $start = $event->start->date;
        }
        printf("%s (%s)\n", $event->getSummary(), $start);
    }
}
